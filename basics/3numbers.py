# there are 3 number data types in python
# int
# float
# complex
# int: Integer data type represents whole numbers without a fractional part.
x = 10
print(x)            # Output: 10
print(type(x))      # Output: <class 'int'>
# float: Float data type represents real numbers with a fractional part.
y = 10.5
print(y)            # Output: 10.5
print(type(y))      # Output: <class 'float'>
# complex: Complex data type represents complex numbers with a real and imaginary part.
z = 3 + 4j
print(z)            # Output: (3+4j)
print(type(z))      # Output: <class 'complex'>
# You can perform various arithmetic operations with these number data types
# Addition
a = x + y
print(a)            # Output: 20.5
# Subtraction
b = y - x
print(b)            # Output: 0.5
# Multiplication
c = x * 2
print(c)            # Output: 20
# Division
d = y / 2
print(d)            # Output: 5.25
# Complex number operations
e = z + (1 + 2j)
print(e)            # Output: (4+6j)
f = z * 2
print(f)            # Output: (6+8j)
# This is a basic overview of number data types in Python.